package ex_7_switch;

public class lab_20 {

    public static void main(String[] args) {

        int a = 1;
        switch (a) {
            default:
                System.out.println("Default");
                break;
            case 1:
                System.out.println("10");
                break;
            case 2:
                System.out.println("9");
                break;
        }
    }
}