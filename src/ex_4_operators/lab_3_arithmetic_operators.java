package ex_4_operators;

public class lab_3_arithmetic_operators {
    public static void main(String[] args){

        int a=45;
        int b=34;
        float f=4.65f;

        System.out.println(a*b);
        System.out.println(a+b);
        System.out.println(a/b);
        System.out.println(a%b);
        // % gives remainder//
        System.out.println(a*f);


    }
}
