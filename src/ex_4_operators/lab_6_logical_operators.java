package ex_4_operators;

public class lab_6_logical_operators {
    public static void main(String[] args)
    {
        boolean g=true;
        boolean j=false;
        boolean h= g || j;
        System.out.println(h);

        boolean a=true;
        System.out.println(!a);
        System.out.println(!!a);
    }
}
